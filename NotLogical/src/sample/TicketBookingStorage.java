    public static String getM_to() {
        return M_to;
    }

    public static void setM_to(String m_to) {
        M_to = m_to;
    }

    public static String getM_From() {
        return M_From;
    }

    public static void setM_From(String m_From) {
        M_From = m_From;
    }

    public static String getM_Phone() {
        return M_Phone;
    }

    public static void setM_Phone(String m_Phone) {
        M_Phone = m_Phone;
    }

    public static String getM_total_price() {
        return M_total_price;
    }

    public static void setM_total_price(String m_total_price) {
        M_total_price = m_total_price;
    }

    public static String getM_seats() {
        return M_seats;
    }

    public static void setM_seats(String m_seats) {
        M_seats = m_seats;
    }


